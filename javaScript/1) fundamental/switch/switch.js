// switch is used when we have to compare a single to multiple different values
const day =  "loveday";

switch (day) {
    case "monday":
        console.log("it's monday");
        break;
    case "tuesday":
        console.log("it's tuesday");
        break;
    case "wednesday":
        console.log("it's wednesday");
        break;
    case "thursday":
        console.log("it's thursday");
        break;
    case "friday":
        console.log("it's friday");
        break;
    case "saturday":
        console.log("it's saturday");
        break;
    case "sunday":
        console.log("it's sunday");
        break;
    default:
        console.log("not valid");
        break;
}